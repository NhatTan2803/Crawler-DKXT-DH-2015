var Crawler = require("crawler");
var url_module = require('url');
var Student = require('../model');

var counter = 0;

var c = new Crawler({
    maxConnections : 500,
    skipDuplicates: true,
    // This will be called for each crawled page
    callback : function (error, result, $) {
    	 var rootUrl = 'http://ts.udn.vn/TracuuView.aspx';
    	//var rootUrl = result.request.href || '';

    	// Get data 
    	var table = $('table tr');
    	// console.log(table);
    	if (table.length) {
    		table.each(function(index, tr) {
	    		// console.log(tr);
	    		var td = $(tr).children('td');
	    		if (td.length) {
					var url_data = url_module.parse(result.request.href, true);
					
	    			var student = {};
	    			student.school_code = url_data.query.matruong;
	    			
					student.faculty = url_data.query.manganh;
					student.faculty_code = student.faculty;
					student.subject_group = getSubjectGroup(url_data.query.tohop);
					
					var id = parseInt($(td[0]).text());
					if (id && id > 0) {
						td.each(function(index, td) {
	    						if (index == 2) student.student_id = $(td).text();
								if (index == 1) student.student_name = $(td).text();
	    						if (index == 3) student.priority = parseInt($(td).text());
	    						if (index == 6) student.score_priority = getScorePriority($(td).text().trim());
								if (index == 5) student.score_sum = parseFloat($(td).text());
		    			});
		    			//console.log(student);
		    			var saver = new Student(student);
						saver.save(function (err, st) {
							console.log('Saved ['+ counter++ +'] ', st.student_id);
						});	
					}
	    		}
	    	});
    	}
		
		// Next page 
    	$('#ctl00_ContentPlaceHolder1_lblPage2 a').each(function(index, a) {
    		var toQueueUrl = rootUrl + $(a).attr('href');
    		console.log("Add to queue: ", toQueueUrl);
    		c.queue(toQueueUrl);
    	});
    },

    onDrain: function() {
    	console.log(">> UDN Module finish.");
    }
});

